function swatchKeyForIndex(source, index) {
  return `${source}-swatch-${index + 1}`;
}

function tagForLabel(label) {
  return String(label || "source")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "");
}

export function createPaletteSourceMockDbRows() {
  let auditIndex = 0;
  return MOCK_DB_SOURCE_GROUPS.flatMap((definition) => definition.swatches.map(([hex, name], index) => ({
    ...createMockDbAuditFields(auditIndex += 1, MOCK_DB_KEYS.users.forgeBot),
    hex,
    id: `${definition.source}-source-swatch-${index + 1}`,
    name,
    source: definition.source,
    sourceLabel: definition.label,
    swatchKey: swatchKeyForIndex(definition.source, index),
    tags: ["source", tagForLabel(definition.label)]
  })));
}
